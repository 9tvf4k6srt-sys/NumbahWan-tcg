#!/usr/bin/env node
/**
 * AI-Sheen Inspect — post-generation visual gate.
 * ─────────────────────────────────────────────────────────────────
 * After image_generation finishes, this tool tells you whether the
 * result actually passes as a real photograph taken on real gear in
 * Taiwan, or whether it leaked AI-render signatures despite a clean
 * prompt. Pre-prompt lint catches what we typed; this catches what
 * the model decided to draw anyway.
 *
 * The visual analysis itself runs through the project's
 * understand_images tool — this script only:
 *   1. accepts an image path/URL + the prompt that produced it
 *   2. emits a structured rubric the caller hands to understand_images
 *   3. parses the rubric response and produces a pass/fail score
 *   4. writes the verdict back to the event bus + a JSON file the
 *      learning-loop reads next pass (so recurring AI-tells become
 *      auto-rules in mem.autoRules)
 *
 * Reused by:
 *   image_generation post-step (call inspect() on every output)
 *   tools/observer-runner.cjs (registry: sheen-inspect observer)
 *   npm run sheen:inspect -- <url> --prompt="..."
 *   require('./tools/ai-sheen-inspect').rubric() / .scoreResponse()
 *
 * Adding a new check: append to RUBRIC_QUESTIONS. The rubric stays
 * in one place and every consumer asks the same questions.
 */

'use strict'

const fs = require('fs')
const path = require('path')

const ROOT = path.resolve(__dirname, '..')
const MYC = path.join(ROOT, '.mycelium')
const EVENTS_FILE = path.join(MYC, 'events.jsonl')
const VERDICT_FILE = path.join(MYC, 'sheen-verdicts.json')

/* ─── The rubric — these questions go to understand_images.
       Each must score 1-10 with one short reason. The caller passes
       this string verbatim as the `instruction` parameter. ──── */
const RUBRIC_QUESTIONS = [
  { id: 'photo_realism',     weight: 2.0, q: 'Does this look like a real photograph taken on real camera gear, or like an AI render? Score 10 = indistinguishable from professional architectural photography, 1 = obviously AI-generated.' },
  { id: 'no_cgi_tells',      weight: 1.5, q: 'Are there CGI/render tells: unnaturally clean surfaces, perfect symmetry, uncanny faces, plastic skin, overly even lighting, suspicious bokeh balls, glowing edges, "AI shimmer"? 10 = none, 1 = many.' },
  { id: 'lighting_honesty',  weight: 1.5, q: 'Is the lighting believable as real on-location office light: mixed color temperatures, uneven exposure, real shadows from real fixtures, NOT dramatic golden-hour hero lighting on every surface? 10 = honest office light, 1 = AI hero lighting.' },
  { id: 'taiwan_locality',   weight: 1.5, q: 'Could this credibly be in Taiwan (Taipei specifically)? Look for: 繁體中文 signage, Taipei skyline through windows, Taiwanese brand names, ROC calendar, scooter culture cues, humid Taipei haze, Taiwan plug sockets. 10 = clearly Taipei, 1 = could be anywhere generic.' },
  { id: 'imperfection_count',weight: 1.5, q: 'Count visible "honest defects" — fingerprints, dust, scuffs, coffee rings, crooked items, cable mess, worn flooring, uneven walls. 10 = at least 5 visible defects, 1 = unnaturally pristine.' },
  { id: 'human_authenticity',weight: 1.0, q: 'If people are visible: do they look like real Taiwanese office workers (varied ages, real body types, candid postures, slight motion blur, imperfect grooming) OR like AI stock-photo people (symmetric faces, perfect skin, hero poses)? 10 = real, 1 = AI stock.' },
  { id: 'screen_authenticity',weight: 1.0, q: 'If digital screens are visible: do they show believable real-world software (Bloomberg Terminal, real spreadsheets, normal browser tabs) OR generic glowing-neural-net AI art? 10 = real software UI, 1 = AI-art screens.' },
  { id: 'depth_and_grain',   weight: 1.0, q: 'Does the image have natural film grain / sensor noise / realistic depth of field, OR does it have the AI "smooth gradient" look? 10 = natural grain, 1 = AI smoothness.' },
  { id: 'material_truth',    weight: 1.0, q: 'Are the materials (wood, leather, brass, glass) consistent and believable, OR do they have AI-render hallmarks (impossible reflections, wrong scale grain, plastic-looking leather)? 10 = real materials, 1 = AI materials.' },
  { id: 'composition_honesty',weight: 0.8, q: 'Is the composition like a working photographer would shoot (slightly off-center, foreground intrusion, imperfect framing) OR like AI defaults (perfectly centered, symmetric, hero composition)? 10 = working-photographer, 1 = AI default.' },
]

/* ─── Build the instruction string for understand_images ───── */
function rubric(prompt) {
  const intro = [
    'You are a forensic image analyst evaluating whether an image looks like a real photograph or an AI generation.',
    '',
    'Context: this image was generated by an AI model with the goal of looking like a real architectural / editorial photograph taken in Taipei, Taiwan, on professional camera equipment (Phase One, Leica, Hasselblad, Canon).',
    '',
    prompt ? `Prompt the AI was given:\n"""\n${prompt.slice(0, 600)}\n"""\n` : '',
    'Evaluate the image against the following rubric. For EACH item, return:',
    '  - A score from 1 to 10',
    '  - A one-sentence reason citing specific visual evidence',
    '',
    'Return your answer as STRICT JSON in this exact shape:',
    '{',
    '  "scores": {',
    '    "photo_realism": { "score": <int>, "reason": "<short>" },',
    '    "no_cgi_tells": { "score": <int>, "reason": "<short>" },',
    '    ... (one entry per rubric id below)',
    '  },',
    '  "ai_tells_observed": ["short string", "short string", ...],',
    '  "taiwan_cues_observed": ["short string", ...],',
    '  "verdict_summary": "<one sentence>"',
    '}',
    '',
    'RUBRIC ITEMS:',
  ].filter(Boolean).join('\n')

  const questions = RUBRIC_QUESTIONS.map((r) => `- ${r.id}: ${r.q}`).join('\n')
  return `${intro}\n${questions}\n\nReturn ONLY the JSON. No prose before or after.`
}

/* ─── Parse a response and compute a weighted score ──────── */
function scoreResponse(rawResponse) {
  /* Tolerate code-fences and surrounding prose */
  let json = null
  const tryParse = (s) => { try { return JSON.parse(s) } catch { return null } }
  json = tryParse(rawResponse)
  if (!json) {
    const m = rawResponse.match(/\{[\s\S]*\}/)
    if (m) json = tryParse(m[0])
  }
  if (!json || !json.scores) {
    return {
      ok: false,
      composite: 0,
      grade: 'F',
      pass: false,
      reason: 'could not parse JSON from inspector response',
      raw: rawResponse.slice(0, 500),
    }
  }

  let weightSum = 0
  let scoreSum = 0
  const breakdown = {}
  const lowItems = []

  for (const r of RUBRIC_QUESTIONS) {
    const entry = json.scores[r.id]
    if (!entry || typeof entry.score !== 'number') {
      breakdown[r.id] = { score: null, reason: 'missing in response', weight: r.weight }
      continue
    }
    const s = Math.max(1, Math.min(10, entry.score))
    breakdown[r.id] = { score: s, reason: entry.reason || '', weight: r.weight }
    scoreSum += s * r.weight
    weightSum += r.weight
    if (s <= 4) lowItems.push({ id: r.id, score: s, reason: entry.reason || '' })
  }

  const composite = weightSum > 0 ? Math.round((scoreSum / weightSum) * 10) : 0  /* 0-100 */
  const grade = composite >= 85 ? 'A' : composite >= 75 ? 'B' : composite >= 65 ? 'C' : composite >= 55 ? 'D' : 'F'
  /* Pass threshold: 75 composite AND no rubric item below 4 */
  const pass = composite >= 75 && lowItems.length === 0

  return {
    ok: true,
    composite,
    grade,
    pass,
    breakdown,
    lowItems,
    aiTells: json.ai_tells_observed || [],
    taiwanCues: json.taiwan_cues_observed || [],
    summary: json.verdict_summary || '',
  }
}

/* ─── Persist verdict + emit event so learning-loop sees it ── */
function recordVerdict(verdict, meta) {
  try {
    if (!fs.existsSync(MYC)) fs.mkdirSync(MYC, { recursive: true })
    let all = []
    try { all = JSON.parse(fs.readFileSync(VERDICT_FILE, 'utf8')) } catch { /* fresh */ }
    if (!Array.isArray(all)) all = []
    all.push({
      ts: Date.now(),
      date: new Date().toISOString(),
      composite: verdict.composite,
      grade: verdict.grade,
      pass: verdict.pass,
      lowItems: verdict.lowItems,
      aiTells: verdict.aiTells,
      taiwanCues: verdict.taiwanCues,
      summary: verdict.summary,
      ...meta,
    })
    /* keep last 200 verdicts only */
    if (all.length > 200) all = all.slice(-200)
    fs.writeFileSync(VERDICT_FILE, JSON.stringify(all, null, 2))
  } catch { /* noop */ }

  try {
    fs.appendFileSync(EVENTS_FILE, JSON.stringify({
      ts: Date.now(), date: new Date().toISOString(),
      system: 'ai-sheen-inspect',
      event: verdict.pass ? 'sheen_pass' : 'sheen_fail',
      severity: verdict.pass ? 'info' : 'warn',
      data: {
        composite: verdict.composite,
        grade: verdict.grade,
        lowItemCount: verdict.lowItems?.length || 0,
        aiTells: verdict.aiTells?.slice(0, 5) || [],
        imageId: meta?.imageId,
        promptHash: meta?.promptHash,
      },
    }) + '\n')
  } catch { /* noop */ }
}

/* ─── Pretty printer ─────────────────────────────────────── */
function pretty(verdict, meta) {
  const lines = []
  const flag = verdict.pass ? '✓ PASS' : '✗ FAIL'
  lines.push('')
  lines.push(`  AI-Sheen Inspect  ${flag}  ${verdict.composite}/${verdict.grade}${meta?.imageId ? `  ${meta.imageId}` : ''}`)
  lines.push(`  ${'─'.repeat(60)}`)
  if (verdict.summary) lines.push(`  ${verdict.summary}`)
  lines.push('')
  for (const r of RUBRIC_QUESTIONS) {
    const b = verdict.breakdown?.[r.id]
    if (!b || b.score == null) continue
    const tag = b.score >= 8 ? '✓' : b.score >= 5 ? '·' : '✗'
    lines.push(`  ${tag} ${r.id.padEnd(22)} ${String(b.score).padStart(2)}/10  ${b.reason.slice(0, 70)}`)
  }
  if (verdict.aiTells?.length) {
    lines.push('')
    lines.push('  AI tells observed:')
    for (const t of verdict.aiTells.slice(0, 8)) lines.push(`    ✗ ${t}`)
  }
  if (verdict.taiwanCues?.length) {
    lines.push('')
    lines.push('  Taiwan cues observed:')
    for (const t of verdict.taiwanCues.slice(0, 5)) lines.push(`    ✓ ${t}`)
  }
  lines.push('')
  return lines.join('\n')
}

/* ─── CLI: takes a JSON-formatted inspector response on stdin
       (the output from understand_images) and produces a verdict.
       Use case:
         understand_images > /tmp/r.json
         node tools/ai-sheen-inspect.cjs --image=<url> --prompt="..." < /tmp/r.json
   ──────────────────────────────────────────────────────── */
if (require.main === module) {
  const argv = process.argv.slice(2)
  const wantJSON = argv.includes('--json')
  const printRubric = argv.includes('--rubric')
  const imageId = (argv.find((a) => a.startsWith('--image=')) || '').split('=').slice(1).join('=') || null
  const promptArg = (argv.find((a) => a.startsWith('--prompt=')) || '').split('=').slice(1).join('=') || ''

  if (printRubric) {
    process.stdout.write(rubric(promptArg) + '\n')
    process.exit(0)
  }

  let raw = ''
  try { raw = fs.readFileSync(0, 'utf8') } catch { /* no stdin */ }
  if (!raw.trim()) {
    process.stderr.write('Usage:\n')
    process.stderr.write('  Print rubric:  node tools/ai-sheen-inspect.cjs --rubric --prompt="..."\n')
    process.stderr.write('  Score response: <understand_images JSON> | node tools/ai-sheen-inspect.cjs --image=URL --prompt="..."\n')
    process.exit(2)
  }

  const verdict = scoreResponse(raw)
  recordVerdict(verdict, { imageId, promptHash: hash(promptArg) })

  if (wantJSON) {
    process.stdout.write(JSON.stringify(verdict, null, 2) + '\n')
  } else {
    process.stdout.write(pretty(verdict, { imageId }))
  }
  process.exit(verdict.pass ? 0 : 1)
}

function hash(s) {
  let h = 0
  for (let i = 0; i < s.length; i++) h = ((h << 5) - h + s.charCodeAt(i)) | 0
  return (h >>> 0).toString(16).slice(0, 8)
}

module.exports = { rubric, scoreResponse, recordVerdict, pretty, RUBRIC_QUESTIONS }
